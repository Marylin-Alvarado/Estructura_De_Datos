    @Override
    public void update(Observable o, Object o1){
        Paciente p = model.getP();
        boolean e = model.isEditar();
        if(e){
            habilitar();
            txtCedula.setEnabled(false);
        }
        txtNombre.setText(p.getNombre());
        txtApellido1.setText(p.getApellido1());
        txtApellido2.setText(p.getApellido2());
        txtCedula.setText(p.getCedula());
        txtPatologia.setText(p.getPatologia());
        txtEdad.setText(p.getEdad());
        txtGenero.setText(p.getGenero());
        Tabla.setModel(model.getTablita());
        txtBuscar.setText("");
        if(p.getFecha() != null){
            try {
                Date j = new SimpleDateFormat("dd/mm/yyyy").parse(p.getFecha());
                jdateFecha.setDate(j);
            } catch (ParseException ex) {
                Logger.getLogger(Vista.class.getName()).log(Level.SEVERE, null, ex);
            }
        }else{
            jdateFecha.setDate(new Date());
        }
        
    }
    
