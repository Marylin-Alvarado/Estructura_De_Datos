    public static boolean guardarArchivo(Paciente  pacientes) {
        try {
            char DIRCARPDATA = 0;
            File archivo = new File(DIRCARPDATA + File.separatorChar + "paciente.dat");
            FileOutputStream salida = new FileOutputStream(archivo);
            ObjectOutputStream ous = new ObjectOutputStream(salida);
            ous.writeObject(pacientes);
            ous.flush();
            ous.close();
            salida.close();
            return true;
        } catch (Exception e) {
            System.out.println("Error: " + e.getMessage());
            return false;
        }
    }

    public static Paciente cargarArchivo() {
        Paciente pacientes = null;
        try {
            char DIRCARPDATA = 0;
            File archivo = new File(DIRCARPDATA + File.separatorChar + "paciente.dat");
            FileInputStream entrada = new FileInputStream(archivo);
            ObjectInputStream ois = new ObjectInputStream(entrada);
            pacientes = (Paciente) ois.readObject();
            ois.close();
            entrada.close();
        } catch (Exception e) {
            System.out.println("Error: " + e.getMessage());
        }
        return pacientes;
    }
    
    public static boolean guardarJson(Paciente pacientes) {
        try {
            char DIRCARPDATA = 0;
            FileWriter archivo = new FileWriter(DIRCARPDATA + File.separatorChar + "paciente.json");
            
            //setPrettyPrinting() se utiliza para formatear el código
            Gson gson = new GsonBuilder().setPrettyPrinting().create();
            archivo.write(gson.toJson(pacientes));
            archivo.flush();
            archivo.close();
            System.out.println(gson.toJson(pacientes));
            return true;
        } catch (Exception e) {
            System.out.println("Error: " + e.getMessage());
            return false;
        }
    }

    public static Paciente cargarJson() {
        Paciente paciente = null;
        try {
            char DIRCARPDATA = 0;
            FileReader archivo = new FileReader(DIRCARPDATA + File.separatorChar + "bolsa.json");
            paciente = new GsonBuilder().create().fromJson(archivo, Paciente.class);            
        } catch (Exception e) {
            System.out.println("Error: " + e.getMessage());
        }
        return paciente;
    }
