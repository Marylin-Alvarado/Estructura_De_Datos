    private boolean valida(String nom, String ape1, String ape2, String cedl, String patolg, String edad, String genero, String gene, String clasif, String clasf, String clasific, String clasifica, String indi, String recet) {
        if(nom.isEmpty()){
            JOptionPane.showMessageDialog(null, "Ingrese Nombre");
            return false;
        }
        if (ape1.isEmpty()){
            JOptionPane.showMessageDialog(null, "Ingrese primer apellido");
            return false;
        }
        if (ape2.isEmpty()){
            JOptionPane.showMessageDialog(null, "Ingrese segundo apellido");
            return false;
        }
        if (cedl.isEmpty()){
            JOptionPane.showMessageDialog(null, "Ingrese cedula");
            return false;
        }
        if (patolg.isEmpty()){
            JOptionPane.showMessageDialog(null, "Ingrese patologia");
            return false;
        }
        if (edad.isEmpty()){
            JOptionPane.showMessageDialog(null, "Ingrese edad");
            return false;
        }
        if (genero.isEmpty()){
            JOptionPane.showMessageDialog(null, "Ingrese genero si es Femenino");
            return false;
        }
        if (gene.isEmpty()){
            JOptionPane.showMessageDialog(null, "Ingrese genero si es Masculino");
            return false;
        }
        if (clasif.isEmpty()){
            JOptionPane.showMessageDialog(null, "Ingrese clasificacion si es niño");
            return false;
        }
        if (clasf.isEmpty()){
            JOptionPane.showMessageDialog(null, "Ingrese clasificacion si es joven");
            return false;
        }
        if (clasific.isEmpty()){
            JOptionPane.showMessageDialog(null, "Ingrese clasificacion si es adulto");
            return false;
        }
        if (clasifica.isEmpty()){
            JOptionPane.showMessageDialog(null, "Ingrese clasificacion si es tercera edad");
            return false;
        }
        if (indi.isEmpty()){
            JOptionPane.showMessageDialog(null, "Ingrese las indicaciones");
            return false;
        }
        if (recet.isEmpty()){
            JOptionPane.showMessageDialog(null, "Ingrese receta");
            return false;
        }
        return true;
    }
    
    private void buttonGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonGuardarActionPerformed
       String nom = txtNombre.getText();
       String ape1 = txtApellido1.getText();
       String ape2 = txtApellido2.getText();
       String cedl = txtCedula.getText();
       String fecha = new SimpleDateFormat("dd/mm/yyyy").format(jdateFecha.getDate());
       String patolg = txtPatologia.getText();
       String edad = txtEdad.getText();
       String genero = txtGenero.getText();
       String gene = txtGeneroM.getText();
       String clasif = txtNiño.getText();
       String clasf = txtJoven.getText();
       String clasific = txtAdulto.getText();
       String clasifica = txtTerceraEdad.getText();
       String indi = txtIndicaciones.getText();
       String recet = txtReceta.getText();
       
       if(valida(nom,ape1,ape2,cedl,patolg,edad,genero,gene,clasif, clasf, clasific, clasifica,indi,recet)){
         control.agregar(model.isEditar(), nom, ape1, ape2, cedl, fecha, patolg,
            edad, genero, gene, clasif, clasf, clasific, clasifica, indi, recet);
       }
       desHabilitar();
       
    }//GEN-LAST:event_buttonGuardarActionPerformed

